-- Prevent double booking trigger
CREATE OR REPLACE FUNCTION check_table_overlap()
RETURNS TRIGGER AS $$
DECLARE
  table_item TEXT;
  overlap_count INT;
BEGIN
  IF NEW.table_number IS NULL OR NEW.status IN ('cancelled', 'rejected') THEN
    RETURN NEW;
  END IF;

  FOR table_item IN SELECT regexp_split_to_table(NEW.table_number, ',\s*') LOOP
    SELECT COUNT(*) INTO overlap_count
    FROM reservations r
    WHERE r.id != NEW.id
      AND r.reservation_date = NEW.reservation_date
      AND r.reservation_time = NEW.reservation_time
      AND r.status IN ('confirmed', 'pending')
      AND EXISTS (
        SELECT 1
        FROM regexp_split_to_table(r.table_number, ',\s*') assigned_table
        WHERE assigned_table = table_item
      );
    
    IF overlap_count > 0 THEN
      RAISE EXCEPTION 'Table % is already reserved for % at %.', table_item, NEW.reservation_date, NEW.reservation_time;
    END IF;
  END LOOP;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER prevent_double_booking
BEFORE INSERT OR UPDATE ON reservations
FOR EACH ROW
EXECUTE FUNCTION check_table_overlap();
