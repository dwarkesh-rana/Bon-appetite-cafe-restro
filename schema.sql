-- 5. Reservations Table
CREATE TABLE reservations (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  customer_name VARCHAR(255) NOT NULL,
  phone VARCHAR(50) NOT NULL,
  email VARCHAR(255) NOT NULL,
  reservation_date DATE NOT NULL,
  reservation_time VARCHAR(50) NOT NULL,
  guests INT NOT NULL,
  table_number VARCHAR(100), -- Holds comma-separated table numbers if combined (e.g. "Table 1, Table 2")
  status VARCHAR(50) DEFAULT 'pending' CHECK (status IN ('pending', 'confirmed', 'rejected', 'cancelled', 'completed')),
  verification_token VARCHAR(255),
  email_status VARCHAR(50) DEFAULT 'pending' CHECK (email_status IN ('pending', 'sent', 'failed')),
  whatsapp_status VARCHAR(50) DEFAULT 'pending' CHECK (whatsapp_status IN ('pending', 'sent', 'failed')),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- 6. Waiting List Table
CREATE TABLE waiting_list (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name VARCHAR(255) NOT NULL,
  phone VARCHAR(50) NOT NULL,
  email VARCHAR(255) NOT NULL,
  date DATE NOT NULL,
  time VARCHAR(50) NOT NULL,
  guests INT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Sequences for order numbers and invoice numbers
CREATE SEQUENCE order_num_seq START WITH 1;
CREATE SEQUENCE invoice_num_seq START WITH 1;

-- 7. Orders Table
CREATE TABLE orders (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  order_number VARCHAR(20) NOT NULL UNIQUE DEFAULT 'BON-' || lpad(nextval('order_num_seq')::text, 4, '0'),
  invoice_number VARCHAR(30) UNIQUE,
  customer_name VARCHAR(255) NOT NULL,
  phone VARCHAR(50),
  table_number VARCHAR(100) NOT NULL,
  reservation_id UUID REFERENCES reservations(id) ON DELETE SET NULL,
  subtotal INT NOT NULL CHECK (subtotal >= 0),
  tax_amount INT NOT NULL CHECK (tax_amount >= 0),
  service_charge INT NOT NULL CHECK (service_charge >= 0),
  final_amount INT NOT NULL CHECK (final_amount >= 0),
  payment_status VARCHAR(50) DEFAULT 'pending_verification' CHECK (payment_status IN ('pending_payment', 'pending_verification', 'paid', 'failed', 'expired')),
  order_status VARCHAR(50) DEFAULT 'pending' CHECK (order_status IN ('pending', 'confirmed', 'preparing', 'ready', 'served', 'cancelled')),
  upi_transaction_id VARCHAR(100) UNIQUE,
  estimated_ready_time INT DEFAULT 15 CHECK (estimated_ready_time >= 0),
  order_session_token_hash VARCHAR(64) NOT NULL,
  payment_expires_at TIMESTAMP WITH TIME ZONE NOT NULL,
  verification_time TIMESTAMP WITH TIME ZONE,
  verified_by VARCHAR(255),
  payment_reference VARCHAR(255),
  confirmed_at TIMESTAMP WITH TIME ZONE,
  preparing_at TIMESTAMP WITH TIME ZONE,
  ready_at TIMESTAMP WITH TIME ZONE,
  served_at TIMESTAMP WITH TIME ZONE,
  completed_at TIMESTAMP WITH TIME ZONE,
  cancelled_at TIMESTAMP WITH TIME ZONE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- 8. Order Items Table (linked to menu_items name to allow read even if name is updated, but with CASCADE block)
CREATE TABLE order_items (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  order_id UUID REFERENCES orders(id) ON DELETE CASCADE,
  food_item VARCHAR(255) REFERENCES menu_items(name) ON DELETE RESTRICT,
  quantity INT NOT NULL CHECK (quantity > 0),
  price INT NOT NULL CHECK (price >= 0),
  special_instruction TEXT
);

-- 9. Audit Logs Table
CREATE TABLE audit_logs (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  actor VARCHAR(255) NOT NULL,
  action VARCHAR(255) NOT NULL,
  details TEXT,
  ip_address VARCHAR(100),
  user_agent TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

